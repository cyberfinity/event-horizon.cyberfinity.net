			<h2>About the contest</h2>
			<p>The object of the <a href="http://en.wikipedia.org/wiki/Nokia_Nseries">N-Series</a> &ldquo;Your Talent&rdquo; contest was to design a cover for Nokia's N76 mobile phone. Submitted designs appeared on the competition website and could be rated by visitors. When the contest finished in August 2007, the creators of the top 10 most popular designs each won an N76 mobile phone with their design printed on.</p>
			<p>Before appearing on the competition website, designs were subject to a manual review by the organisers to ensure that they complied with the rules (i.e. no copyright violations or offensive content). The approval or rejection of a design by the reviewers was supposed to happen within a "few days" but in James's case took much longer. His <a href="dirtydoodles/">Dirty Doodles</a> design was approved on 11. August - <em>the last day of the contest</em> - and the other designs were neither approved nor rejected at that point. Oddly, those designs were eventually approved and put on the site on 24. August, the day <em>after</em> the winners were announced!</p>
			<p>With barely a day to promote his design and for visitors to vote for it, <a href="dirtydoodles/">Dirty Doodles</a> did not achieve a good rank, let alone the top 10. If it had been approved and appeared on the competition website earlier it may well have had better chances.</p>

